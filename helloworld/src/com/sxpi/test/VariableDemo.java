package com.sxpi.test;

public class VariableDemo {
    public static void main(String[] args) {
        //目标理解变量的使用
        //数据类型  变量名称=初始值
        double money=6.0;
        System.out.println(money);
        //收红包（从右往左看）
        money=money+4.0;
        System.out.println(money);

        System.out.println("---------");
        int age=21;
        System.out.println(age);

        age=25;
        System.out.println(age);
    }
}
